function add(a, b) {
    return a + b;
}

function sub(a, b) {
    return a - b;
}

function div(a, b) {
    return a / b;
}

function mult(a, b) {
    return a * b;
}

function pow(a, b) {
    return a ** b;
}

function max(a, b) {
    return a > b ? a : b;
}

module.exports = {
    add,
    sub,
    div,
    mult,
    pow,
    max,
};
